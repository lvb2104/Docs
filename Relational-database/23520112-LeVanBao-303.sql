CREATE TRIGGER trigger_23
ON CHITIETDT
FOR INSERT
AS
BEGIN 
	UPDATE DONTHUOC
	SET TRIGIADT = (
		SELECT SUM(THANHTIEN)
		FROM CHITIETDT
		WHERE DONTHUOC.MADT = CHITIETDT.MADT
	)
	WHERE DONTHUOC.MADT IN (
		SELECT MADT
		FROM inserted
	)
END

