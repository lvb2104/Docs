JOIN KHAMBENH ON KHAMBENH.MABN = BENHNHAN.MABN
JOIN DONTHUOC ON DONTHUOC.MAKB = KHAMBENH.MAKB
WHERE YEAR(NGAYCAPTHUOC) = 2024 AND TINHTRANGDT = 'Da thanh toan' AND TONGTIENTT < 100000


