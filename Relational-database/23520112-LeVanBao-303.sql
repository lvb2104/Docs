SELECT BENHNHAN.MABN, HOTENBN
FROM (
	SELECT TOP 1 WITH TIES MABN
	FROM KHAMBENH
	GROUP BY MABN
	ORDER BY COUNT(MAKB) ASC
) AS BENHNHANKHAMBENHITNHAT 
