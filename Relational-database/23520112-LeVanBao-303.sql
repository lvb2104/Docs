CREATE TABLE BENHNHAN
(
	MABN char(5) PRIMARY KEY,
	HOTENBN nvarchar(50),
	NGAYSINH smalldatetime,
	CCCD nvarchar(12),
	SOBHYT nvarchar(15),
	BHYTCHITRA float,
	DIACHI nvarchar(100),
)

CREATE TABLE BACSI
(
	MABS char(5) PRIMARY KEY,
	HOTENBS nvarchar(50),
	NGAYBDLV smalldatetime,
	CHUYENKHOA nvarchar(50),
)

CREATE TABLE KHAMBENH
(
	MAKB char(5) PRIMARY KEY,
	MABN char(5),
	MABS char(5),
	NGAYKHAM smalldatetime,
	TRIEUCHUNG nvarchar(255),
	KETLUAN nvarchar(255),
	TAIKHAM int,
	FOREIGN KEY (MABN) REFERENCES BENHNHAN(MABN),
	FOREIGN KEY (MABS) REFERENCES BACSI(MABS)
)

CREATE TABLE THUOC
(
	MATHUOC char(5) PRIMARY KEY,
	TENTHUOC nvarchar(50),
	LOAITHUOC nvarchar(50),
	DVT nvarchar(20),
	DONGIA money,
)

CREATE TABLE DONTHUOC
(
	MADT char(5) PRIMARY KEY,
	MAKB char(5),
	TRIGIADT money,
	BHYTCHITRA float,
	NGAYCAPTHUOC smalldatetime,
	TONGTIENTT money,
	TINHTRANGDT nvarchar(30),
	FOREIGN KEY (MAKB) REFERENCES KHAMBENH(MAKB)
)

CREATE TABLE CHITIETDT
(
	MADT char(5),
	MATHUOC char(5),
	SOLUONG int,
	THANHTIEN money,
	FOREIGN KEY (MATHUOC) REFERENCES THUOC(MATHUOC),
	FOREIGN KEY (MADT) REFERENCES DONTHUOC(MADT)
)


