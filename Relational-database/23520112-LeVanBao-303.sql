SELECT BACSI.MABS, HOTENBS, BENHNHAN.MABN, HOTENBN
FROM BACSI
JOIN KHAMBENH ON KHAMBENH.MABS = BACSI.MABS
JOIN BENHNHAN ON BENHNHAN.MABN = KHAMBENH.MABN
WHERE CHUYENKHOA = 'Tai mui hong' AND YEAR(NGAYKHAM) = 2024

-- 3.2
SELECT BENHNHAN.MABN, HOTENBN
FROM BENHNHAN
JOIN KHAMBENH ON KHAMBENH.MABN = BENHNHAN.MABN
WHERE YEAR(NGAYKHAM) = 2024 AND EXISTS (
	SELECT 1
	FROM DONTHUOC
	WHERE DONTHUOC.MAKB = KHAMBENH.MAKB AND TINHTRANGDT = 'Chua thanh toan' AND TONGTIENTT >= 100000
)

-- 3.3
SELECT DONTHUOC.MADT, MAKB
FROM DONTHUOC
JOIN CHITIETDT ON CHITIETDT.MADT = DONTHUOC.MADT
JOIN THUOC ON THUOC.MATHUOC = CHITIETDT.MATHUOC
WHERE YEAR(NGAYCAPTHUOC) = 2024 AND TINHTRANGDT = 'Da thanh toan' AND LOAITHUOC = 'Thuoc di ung'
GROUP BY DONTHUOC.MADT, MAKB
HAVING COUNT(DISTINCT THUOC.MATHUOC) = (
	SELECT COUNT(DISTINCT MATHUOC)
	FROM THUOC
	WHERE LOAITHUOC = 'Thuoc di ung'
)

-- 3.4
SELECT BENHNHAN.MABN, HOTENBN, COUNT(MADT)
FROM BENHNHAN
JOIN KHAMBENH ON KHAMBENH.MABN = BENHNHAN.MABN
JOIN DONTHUOC ON DONTHUOC.MAKB = KHAMBENH.MAKB
WHERE YEAR(NGAYCAPTHUOC) = 2024 AND TINHTRANGDT = 'Da thanh toan' AND SOBHYT IS NULL
GROUP BY BENHNHAN.MABN, HOTENBN

